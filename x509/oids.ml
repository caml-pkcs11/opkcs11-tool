(* Helpers with oids *)
let get_oids_from_name name oid_list =
  let the_list = List.find_all (fun (a, b) -> compare name a = 0) oid_list in
  (* Return the list of oid candidates *)
  let (names_list, oids_list) = List.split the_list in
  (oids_list)

let get_oids_from_names names_list oids_list =
  (List.concat (List.map (
    fun name -> get_oids_from_name name oids_list
  ) names_list
  ))

let get_name_from_oid oid oid_list = 
  let (a, b) = (try List.find (fun (a, b) -> compare oid b = 0) oid_list
    with Not_found -> ("", [||])) in
  (a)

let check_is_oid_in_list oid oid_list =
  let check = (try List.find (fun a -> compare a oid = 0) oid_list
    with Not_found -> [||]) in
  if compare check [||] = 0 then
    (false)
  else
    (true)
